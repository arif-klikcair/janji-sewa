  <span class="section-title">FAQ JANJISEWA</span>
  <div class="section-content">
    <ol>
      <li class="judul-list">Apa itu janjisewa?</li>
      <div class="list-content">
        <p>janjisewa adalah platform listing yang memfasilitasi perjanjian sewa menyewa antar pelaku UMKM (khususnya bidang kuliner & groceries) dengan penyedia tempat agar perjanjiannya bersifat sah dan tak bertentangan dengan peraturan perundang-undangan.</p>
      </div>
      <li class="judul-list">Bagaimana cara kerja janjisewa?</li>
      <div class="list-content">
        <ul>
          <li><strong>Sebagai Penyedia Tempat</strong>, anda hanya perlu mendaftarkan sebagai Penyedia Tempat kemudian menginput data ruko/tempat sewaan anda dan mencantumkan kontak anda dalam waktu maksimal 24 jam, tempat sewaan anda akan masuk di listing kami. Kemudian kami akan merekomendasikan tempat sewaan anda kepada para Pelaku Usaha dan mereka akan menghubungi anda untuk proses negosiasi. Bila negosiasi berjalan dengan baik, anda dapat mengunduh perjanjian sewa menyewa di website janjisewa.</li>
          <li><strong>Sebagai Pelaku Usaha</strong>, anda dapat mendaftarkan diri sebagai Pelaku Usaha kemudian melengkapi data usaha anda. Kemudian anda dapat mencari dan menemukan tempat-tempat sewaan yang sesuai dengan kebutuhan anda. Pelaku Usaha akan menghubungi Penyedia Tempat untuk mendapatkan informasi lebih lanjut kemudian melakukan negosiasi. Bila negosiasi berjalan dengan baik, anda dapat mengunduh perjanjian sewa menyewa di website janjisewa.</li>
        </ul>
      </div>
      <li class="judul-list">Bagaimana cara mendaftar sebagai Penyedia Tempat?</li>
      <div class="list-content">
        <p>Anda dapat mendaftar dengan link ______ atau memilih pilihan ‘Mendaftar sebagai Penyedia Tempat’ di halaman utama kami. Dalam pendaftaran ini anda akan langsung diminta untuk mendaftarkan tempat yang akan anda sewakan dengan minimal 3 Foto yang memiliki pencahayaan yang jelas dan anda harus mengisi kelengkapan wajib sebagai informasi atas tempat sewaan anda untuk menarik minat user.</p>
      </div>
      <li class="judul-list">Bagaimana cara mendaftar sebagai Pelaku usaha?</li>
      <div class="list-content">
        <p>Anda dapat mendaftar dengan link ______ atau memilih pilihan ‘Mendaftar sebagai Pelaku Usaha’ di halaman utama kami. Dalam pendaftaran ini anda akan diminta untuk mengisi data diri anda dan data usaha anda.</p>  
      </div>
      <li class="judul-list">Bagaimana cara menghubungi Penyedia Tempat yang saya minati?</li>
      <div class="list-content">
        <p>Anda dapat menghubungi tim janjisewa melalui email dan kami akan memberikan kontak Penyedia Tempat kepada anda.</p>
      </div>
      <li class="judul-list">Apakah Penyedia Tempat yang listing disini kredibel?</li>
      <div class="list-content">
        <p>janjisewa tidak menjamin kredibilitas dari setiap Penyedia tempat dan Pelaku Usaha, sehingga anda harus secara sadar dan berhati-hati dalam memilih tempat sewa dan menerima penyewa</p>
      </div>
      <li class="judul-list">Saya seorang agen properti, bisakah saya listing disini?</li>
      <div class="list-content">
        <p>Bisa, bila anda bukanlah pemilik dari tempat yang disewakan, anda juga dapat listing di janjisewa.</p>
      </div>
      <li class="judul-list">Bagaimana proses perjanjian sewa yang dilakukan di janjisewa?</li>
      <div class="list-content">
        <p>Kedua pihak yaitu Pelaku Usaha dan Penyedia Tempat dapat mendownload format perjanjian sewa yang dapat diubah sesuai kebutuhan, perjanjian sewa ini sudah sesuai dengan undang-undang yang berlaku.</p>
      </div>
    </ol>
