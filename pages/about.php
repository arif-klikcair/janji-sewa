<section>
  <span class="section-title">Perkenalkan, kami adalah janjisewa.</span>
  <div class="section-content">
    <p>janjisewa adalah platform listing yang memfasilitasi perjanjian sewa menyewa antara <strong>Pelaku Usaha</strong> UMKM, khususnya di bidang kuliner & groceries, dengan <strong>Penyedia Tempat</strong>.</p>
    <p>Kami membantu <strong>Pelaku Usaha</strong> dan <strong>Penyedia Tempat</strong> untuk memiliki surat perjanjian sewa-menyewa yang sah dan tidak bertentangan dengan peraturan perundang-undangan.</p>
    <p>Dengan begitu, <strong>Pelaku Usaha</strong> tidak perlu khawatir saat ingin menyewa kios, ruko, toko, atau bahkan lahan yang akan digunakan untuk mengembangkan bisnisnya.</p>
    <p>Begitu juga dengan <strong>Penyedia Tempat</strong> tidak perlu takut lagi menyewakan kios, ruko, toko, atau lahan miliknya yang tersedia.</p>
    <p>Sebab, kedua belah pihak bisa bertemu dengan mitra yang potensial dan masing-masing akan memperoleh manfaat serta keuntungan.</p>
    <span class="tagline">Bersama janjisewa, semua <strong>tercatat</strong>, semua <strong>nyaman</strong>.</span>
  </div>
</section>
